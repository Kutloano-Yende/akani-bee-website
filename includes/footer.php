  if (window.scrollY > 400) {
    scrollBtn.classList.remove('opacity-0', 'invisible');
    scrollBtn.classList.add('opacity-100', 'visible');
  } else {
    scrollBtn.classList.add('opacity-0', 'invisible');
    scrollBtn.classList.remove('opacity-100', 'visible');
  }
});
</script>
